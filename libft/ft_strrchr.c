#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*last_occurrence;

	last_occurrence = NULL;
	while (*s)
	{
		if (*s == (char)c)
			last_occurrence = (char*)s;
		++s;
	}
	if (!last_occurrence && c == '\0')
		last_occurrence = (char*)s;
	return (last_occurrence);
}
