#include <unistd.h>
#include "libft.h"

void	ft_putendl_fd(const char *s, int fd)
{
	if (s != NULL && fd != -1)
	{
		write(fd, s, ft_strlen(s));
		ft_putchar_fd('\n', fd);
	}
}
